def convert2Hot(x):
    ir2RGB = IronHot()
    m = x.shape[0]
    n = x.shape[1]
    s = (m, n, 3)
    YRGB = np.zeros(s)
    for i in range(m):
        for j in range(n):
            YRGB[i][j][0] = ir2RGB[0][x[i][j]]
            YRGB[i][j][1] = ir2RGB[1][x[i][j]]
            YRGB[i][j][2] = ir2RGB[2][x[i][j]]
    YRGB = YRGB/255.0
    return YRGB


